////////////////////////////////////////////////////////////////////////////////

function min(argOne, argTwo) {
  if (argOne > argTwo){ //compares two args
    return argTwo; //returns arg2 if arg1 is bigger
  } else {  //OR if not
    return argOne; //returns arg1
  }
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num) {
  //base case
  if (num < 0){ //what to do if negative, opposite count
    return isEven(num +2);
  }
  if (num === 0){ //eventually get to this number which is even
  return true;
  };
  if (num === 1){ //or eventually get to this number, odd
  return false;
  }
  //recursion
  return isEven(num - 2); //number has to be reduced by 2 to find 0 or 1

}

