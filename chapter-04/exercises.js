////////////////////////////////////////////////////////////////////////////////
// nth /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function nth(list, n) {
  if (!list){ 
    return undefined;
  }else if (n === 0){
    return list.value;
  }else {
    return nth(list.rest, n - 1)
  }
}

