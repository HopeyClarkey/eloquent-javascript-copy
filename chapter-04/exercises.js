// sum /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function sum(array){
  let output = 0; // create output value
  for(let i = 0; i < array.length; i++){ //loop through array
    output += array[i]; //add each iteration to output
  }
  return output; //return that output
}

